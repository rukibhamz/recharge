create or replace view public.personality_questions_full as
  select
    q.id,
    q.question_number,
    q.question_text,
    q.scored_pole,
    d.code as dichotomy,
    d.pole_a,
    d.pole_b,
    d.label_a,
    d.label_b
  from public.personality_questions q
  join public.personality_dichotomies d on d.id = q.dichotomy_id
  order by q.question_number;
