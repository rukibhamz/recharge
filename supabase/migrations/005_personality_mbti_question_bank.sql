-- MBTI-style personality test (4 dichotomies × 30 questions = 120)
-- 5-point agreement scale; 16 type profiles

create table if not exists public.personality_dichotomies (
  id serial primary key,
  code char(3) not null unique,
  pole_a char(1) not null,
  pole_b char(1) not null,
  label_a text not null,
  label_b text not null,
  description text,
  created_at timestamptz default now()
);

