on conflict (code) do nothing;

create table if not exists public.personality_questions (
  id serial primary key,
  dichotomy_id int not null references public.personality_dichotomies(id) on delete cascade,
  question_number int not null unique,
  question_text text not null,
  scored_pole char(1) not null,
  created_at timestamptz default now()
);

